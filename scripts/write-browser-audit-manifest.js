import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { basename, join, resolve } from 'node:path';
import { execFileSync } from 'node:child_process';

const repoRoot = resolve(import.meta.dirname, '..');
const auditDir = join(repoRoot, 'tests', 'browser', 'audit');
const manifestPath = join(auditDir, 'MANIFEST.md');

function git(args) {
  return execFileSync('git', args, {
    cwd: repoRoot,
    encoding: 'utf8',
  }).trim();
}

const captures = readdirSync(auditDir)
  .filter((name) => name.endsWith('.png'))
  .sort()
  .map((name) => {
    const fullPath = join(auditDir, name);
    const stats = statSync(fullPath);
    return {
      name,
      size: stats.size,
      modifiedAt: stats.mtime.toISOString(),
    };
  });

const generatedAt = new Date().toISOString();
const commitSha = git(['rev-parse', 'HEAD']);
const branchName = git(['rev-parse', '--abbrev-ref', 'HEAD']);
const version = JSON.parse(readFileSync(join(repoRoot, 'package.json'), 'utf8')).version;

const lines = [
  '---',
  'title: Browser Audit Manifest',
  `updated: ${generatedAt.slice(0, 10)}`,
  'status: current',
  'domain: qa',
  '---',
  '# Browser Audit Manifest',
  '',
  'Generated by `pnpm audit:browser-captures` after the staged browser capture flow completes.',
  '',
  '## Capture Context',
  '',
  `- Generated at: \`${generatedAt}\``,
  `- Git commit: \`${commitSha}\``,
  `- Git branch: \`${branchName}\``,
  `- Package version: \`${version}\``,
  `- Source test: \`tests/browser/current-flow-captures.test.tsx\``,
  `- Refresh command: \`pnpm audit:browser-captures\``,
  '',
  '## Captures',
  '',
  '| File | Modified (UTC) | Bytes |',
  '| --- | --- | ---: |',
  ...captures.map(
    ({ name, modifiedAt, size }) =>
      `| [${name}](/Users/jbogaty/src/arcade-cabinet/pond-warfare/tests/browser/audit/${basename(name)}) | \`${modifiedAt}\` | ${size} |`,
  ),
  '',
];

writeFileSync(manifestPath, `${lines.join('\n')}\n`, 'utf8');
